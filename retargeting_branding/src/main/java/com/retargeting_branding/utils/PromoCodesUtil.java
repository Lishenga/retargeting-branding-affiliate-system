package com.retargeting_branding.utils;

import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Service;

import io.jsonwebtoken.Claims;
import io.jsonwebtoken.Jwts;
import io.jsonwebtoken.SignatureAlgorithm;

import java.util.Date;
import java.util.HashMap;
import java.util.Map;
import java.util.function.Function;

@Service
public class PromoCodesUtil {
    
    @Value("${system-config.promocode.generator}")
    private String promoCodeGenerator;

    public String extractAdvertiser(String token) {
        return extractClaim(token, Claims::getSubject);
    }

    public <T> T extractClaim(String token, Function<Claims, T> claimsResolver) {
        final Claims claims = extractAllClaims(token);
        return claimsResolver.apply(claims);
    }
    private Claims extractAllClaims(String token) {
        return Jwts.parser().setSigningKey(promoCodeGenerator).parseClaimsJws(token).getBody();
    }

    public String generateToken(String identifier) {
        Map<String, Object> claims = new HashMap<>();
        return createToken(claims, identifier);
    }

    private String createToken(Map<String, Object> claims, String subject) {

        return Jwts.builder().setClaims(claims).setSubject(subject).setIssuedAt(new Date(System.currentTimeMillis()))
                .signWith(SignatureAlgorithm.HS256, promoCodeGenerator).compact();
    }
}